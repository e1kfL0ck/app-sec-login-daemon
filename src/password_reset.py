# Workflow:
# 1. User clicks on "Forgot Password" link on login page.
# 2. User is taken to a form to enter their email address.
# 3. If the email exists, a password reset token is generated and emailed to the user.
# 4. User clicks the link in the email, which takes them to a form to enter a new password.
# 5. User submits the new password, which is validated and updated in the database.
# 6. User is informed of the successful password reset, and taken back to the login page.


